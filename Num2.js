//Crea un programa que calcule la superficie de un rectángulo a 
//partir de su base y su altura, y que después muestre el valor de dicha superficie.

let base=Number(prompt("Por Favor, ingrese la base del rectángulo."))
let altura=Number(prompt("Por Favor, ingrese la altura del rectángulo."))
let result=base*altura;

document.write("------------------------------------------------------------------------------------");
document.write("<br>")
document.write("La superficie del rectángulo es: ", result);
document.write("<br>")
document.write("------------------------------------------------------------------------------------");